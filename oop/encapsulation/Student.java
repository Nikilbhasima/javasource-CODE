package oop.encapsulation;

public class Student {
    private int id;
    private String name;
    public void setid(int id){
        this.id=id;
    }
    public int getid(){
        return this.id;
    }
    public void setName(String name){
        this.name=name;
    }
    public String getName(){
        return this.name;
    }
}
